package kz.azh.model;

public class Student {
	
	private String name;
	private String group;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		name = name;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		group = group;
	}
}
